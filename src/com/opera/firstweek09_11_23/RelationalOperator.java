package com.opera.firstweek09_11_23;

public class RelationalOperator 
{
	public boolean lessthanOperator(int value1,int value2) {
		return (value1<value2);
    }
	public boolean greaterthanOperator(int value1,int value2) {
		return (value1>value2);
    }
	public boolean greaterthanOrEqualToOperator(int value1,int value2) {
		return (value1<=value2);
    }
	public boolean lessthanOrEqualToOperator(int value1,int value2) {
		return (value1>=value2);
    }
	public boolean gequalToOperator(int value1,int value2) {
		return (value1==value2);
    }
	public boolean notEqualToOperator(int value1,int value2) {
		return (value1!=value2);
    }
	
}
